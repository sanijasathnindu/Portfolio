/* ==========================
   PROJECT GRID
========================== */

function renderProjectGrid(projectList) {

    const grid = document.getElementById('projectsGrid');

    if (!grid) return;

    grid.innerHTML = '';

    projectList.forEach((project, index) => {

        const card = document.createElement('button');

        card.type = 'button';
        card.className = 'grid-card';

        card.style.animationDelay =
            `${index * 0.05}s`;

        card.innerHTML = `
            <span>

                <h3>
                    ${escapeHTML(project.title)}
                </h3>

                <p>
                    ${escapeHTML(project.short_description)}
                </p>

                <div class="project-tags">
                    ${(project.technologies || [])
                        .slice(0, 4)
                        .map(tag =>
                            `<span class="mini-tag">${escapeHTML(tag)}</span>`
                        )
                        .join('')}
                </div>

            </span>

            <span class="card-meta">
                View Details
            </span>
        `;

        card.onclick = () =>
            launchProjectPresenter(project);

        grid.appendChild(card);
    });
}

/* ==========================
   FLOATING PROJECT BAND
========================== */

function renderFloatingProjects(projectList) {

    const band =
        document.getElementById('dynamicFloatBand');

    const lane =
        document.getElementById('floatingProjects');

    if (!band || !lane) return;

    if (projectList.length <= 4) {

        band.style.display = 'none';

        return;
    }

    band.style.display = 'block';

    lane.innerHTML = '';

    [...projectList, ...projectList].forEach(project => {

        const item =
            document.createElement('div');

        item.className = 'float-project';

        item.innerHTML = `
            <h3>
                ${escapeHTML(project.title)}
            </h3>

            <p>
                ${escapeHTML(project.short_description)}
            </p>
        `;

        item.onclick = () =>
            launchProjectPresenter(project);

        lane.appendChild(item);
    });
}

/* ==========================
   FILTERS
========================== */

function initializeProjectFilters() {

    const buttons =
        document.querySelectorAll('.filter-pill');

    if (!buttons.length) return;

    buttons.forEach(button => {

        button.addEventListener('click', () => {

            buttons.forEach(btn =>
                btn.classList.remove('active')
            );

            button.classList.add('active');

            const filter =
                button.dataset.filter;

            if (filter === 'all') {

                renderProjectGrid(projects);

                return;
            }

            const filtered =
                projects.filter(project => {

                    const tech =
                        project.technologies || [];

                    return tech.includes(filter);
                });

            renderProjectGrid(filtered);
        });
    });
}

/* ==========================
   GLASS BACKGROUND
========================== */

function spawnAbstractCanvasElements() {

    const matrix =
        document.getElementById('matrixGlow');

    if (!matrix) return;

    matrix.innerHTML = '';

    for (let i = 0; i < 18; i++) {

        const node =
            document.createElement('div');

        node.className =
            'floating-glass-node';

        const size =
            Math.random() * 120 + 80;

        node.style.width =
            `${size}px`;

        node.style.height =
            `${size}px`;

        node.style.left =
            `${Math.random() * 100}vw`;

        node.style.animationDelay =
            `${Math.random() * 12}s`;

        node.style.animationDuration =
            `${Math.random() * 12 + 16}s`;

        matrix.appendChild(node);
    }
}

/* ==========================
   MODAL HELPERS
========================== */

function openModal(id) {

    const modal =
        document.getElementById(id);

    if (!modal) return;

    modal.classList.add('is-open');

    modal.setAttribute(
        'aria-hidden',
        'false'
    );
}

function closeModal(id) {

    const modal =
        document.getElementById(id);

    if (!modal) return;

    modal.classList.remove('is-open');

    modal.setAttribute(
        'aria-hidden',
        'true'
    );
}

/* ==========================
   SECURITY
========================== */

function escapeHTML(value = '') {

    return String(value).replace(
        /[&<>"']/g,
        character => ({
            '&': '&amp;',
            '<': '&lt;',
            '>': '&gt;',
            '"': '&quot;',
            "'": '&#039;'
        }[character])
    );
}