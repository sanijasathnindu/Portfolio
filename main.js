/* ==========================
   PROJECT GRID
========================== */

function renderProjectGrid(projectList) {

    const grid = document.getElementById('projectsGrid');

    if (!grid) return;

    grid.innerHTML = '';

    projectList.forEach((project, index) => {

        const card = document.createElement('button');

        card.type = 'button';
        card.className = 'grid-card';

        card.style.animationDelay =
            `${index * 0.05}s`;

        card.innerHTML = `
            <span>

                <h3>
                    ${escapeHTML(project.title)}
                </h3>

                <p>
                    ${escapeHTML(project.short_description)}
                </p>

                <div class="project-tags">
                    ${(project.technologies || [])
                        .slice(0, 4)
                        .map(tag =>
                            `<span class="mini-tag">${escapeHTML(tag)}</span>`
                        )
                        .join('')}
                </div>

            </span>

            <span class="card-meta">
                View Details
            </span>
        `;

        card.onclick = () =>
            launchProjectPresenter(project);

        grid.appendChild(card);
    });
}

/* ==========================
   FLOATING PROJECT BAND
========================== */

function renderFloatingProjects(projectList) {

    const band =
        document.getElementById('dynamicFloatBand');

    const lane =
        document.getElementById('floatingProjects');

    if (!band || !lane) return;

    if (projectList.length <= 4) {

        band.style.display = 'none';

        return;
    }

    band.style.display = 'block';

    lane.innerHTML = '';

    [...projectList, ...projectList].forEach(project => {

        const item =
            document.createElement('div');

        item.className = 'float-project';

        item.innerHTML = `
            <h3>
                ${escapeHTML(project.title)}
            </h3>

            <p>
                ${escapeHTML(project.short_description)}
            </p>
        `;

        item.onclick = () =>
            launchProjectPresenter(project);

        lane.appendChild(item);
    });
}

/* ==========================
   FILTERS
========================== */

function initializeProjectFilters() {

    const buttons =
        document.querySelectorAll('.filter-pill');

    if (!buttons.length) return;

    buttons.forEach(button => {

        button.addEventListener('click', () => {

            buttons.forEach(btn =>
                btn.classList.remove('active')
            );

            button.classList.add('active');

            const filter =
                button.dataset.filter;

            if (filter === 'all') {

                renderProjectGrid(projects);

                return;
            }

            const filtered =
                projects.filter(project => {

                    const tech =
                        project.technologies || [];

                    return tech.includes(filter);
                });

            renderProjectGrid(filtered);
        });
    });
}

/* ==========================
   GLASS BACKGROUND
========================== */

function spawnAbstractCanvasElements() {

    const matrix =
        document.getElementById('matrixGlow');

    if (!matrix) return;

    matrix.innerHTML = '';

    for (let i = 0; i < 18; i++) {

        const node =
            document.createElement('div');

        node.className =
            'floating-glass-node';

        const size =
            Math.random() * 120 + 80;

        node.style.width =
            `${size}px`;

        node.style.height =
            `${size}px`;

        node.style.left =
            `${Math.random() * 100}vw`;

        node.style.animationDelay =
            `${Math.random() * 12}s`;

        node.style.animationDuration =
            `${Math.random() * 12 + 16}s`;

        matrix.appendChild(node);
    }
}

/* ==========================
   MODAL HELPERS
========================== */

function openModal(id) {
    const modal = document.getElementById(id);
    if (!modal) return;

    modal.classList.add('is-open');
    modal.setAttribute('aria-hidden', 'false');

    // wait for layout paint properly
    requestAnimationFrame(() => {
        requestAnimationFrame(() => {
            modal.dispatchEvent(new Event("modal-opened"));
        });
    });
}

function closeModal(id) {

    const modal =
        document.getElementById(id);

    if (!modal) return;

    modal.classList.remove('is-open');

    modal.setAttribute(
        'aria-hidden',
        'true'
    );
}

/* ==========================
   Paypal Integration
========================== */

const PayPalCheckout = (function () {

  let sdkLoaded = false;

  // -----------------------------
  // Load PayPal SDK only once
  // -----------------------------
function loadSDK(clientId, currency) {
    return new Promise((resolve, reject) => {

        if (window.paypal?.Buttons) return resolve();

        const script = document.createElement("script");
        script.src =
          `https://www.paypal.com/sdk/js?client-id=${clientId}&currency=${currency}&intent=capture&components=buttons&disable-funding=credit`;

        script.onload = () => {
            resolve();
        };

        script.onerror = () => {
            reject("PayPal SDK failed to load");
        };

        document.head.appendChild(script);
    });
}

  // -----------------------------
  // Token generator
  // -----------------------------
  function generateToken() {
    return (
      "tok_" +
      Math.random().toString(36).substring(2, 10) +
      Date.now()
    );
  }

  // -----------------------------
  // Main reusable button
  // -----------------------------
  async function createPayPalButton({
    containerId,
    clientId,
    productId,
    productName,
    price,
    currency = "USD",
    successRedirect = "success.html",
    metadata = {}
  }) {

    await loadSDK(clientId, currency);

    // prevent duplicate render
    const container = document.getElementById(containerId);
    if (!container) return;

    container.innerHTML = "";

    window.paypal.Buttons({

      style: {
        layout: "vertical",
        color: "gold",
        shape: "rect",
        label: "paypal"
      },

      // -----------------------------
      // Create Order
      // -----------------------------
      createOrder: (data, actions) => {
        return actions.order.create({
          purchase_units: [{
            description: productName,
            amount: {
              currency_code: currency,
              value: Number(price).toFixed(2)     // Required: convert number to string
            },
            custom_id: productId,
            invoice_id: "INV-" + Date.now()
          }]
        });
      },

      // -----------------------------
      // Payment Approved
      // -----------------------------
      onApprove: (data, actions) => {
        return actions.order.capture().then(() => {

          console.log("PAYPAL CAPTURE SUCCESS");

            try {

            const token = generateToken();

            let purchases = JSON.parse(localStorage.getItem("purchases") || "{}");

            if (purchases[productId]?.paid) {
                alert("You already purchased this item.");
                window.location.href = `${successRedirect}?token=${purchases[productId].token}`;
                return;
            }

            purchases[productId] = {
                paid: true,
                token: token,
                time: Date.now(),
                meta: {
                productName,
                price,
                ...metadata
                }
            };

            localStorage.setItem("purchases", JSON.stringify(purchases));

            let tokens = JSON.parse(localStorage.getItem("tokens") || "{}");

            tokens[token] = {
                productId,
                used: false,
                downloadLink: metadata?.downloadLink || ""
            };

            localStorage.setItem("tokens", JSON.stringify(tokens));

            sessionStorage.setItem("last_purchase", productId);

            const redirectURL = `${successRedirect}?token=${token}`;

            console.log("REDIRECTING:", redirectURL);

            window.location.assign(redirectURL);

            } catch (err) {
            console.error("PAYMENT ERROR:", err);
            alert("Payment succeeded but something broke. Check console.");
            }

        });
      }

    }).render(`#${containerId}`);
  }

  return {
    createPayPalButton
  };

})();

